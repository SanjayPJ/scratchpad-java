package com.example.learnjava;

public class variables {
    public static void main(String[] args) {

        //storing strings

        String name = "Sanjay PJ";
        System.out.println(name);

        //storing int values into variables

        int totalBounty = 56000;
        System.out.println(name + " has " + totalBounty + " total bounty.");

        //storing floating values

        float totalBankBalance = 7.3435f;

        float finalBalance = totalBankBalance - 45;

        System.out.println("Final Balance of " + name + " is " + finalBalance);




    }
}
